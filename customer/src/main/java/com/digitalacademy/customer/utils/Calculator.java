package com.digitalacademy.customer.utils;

public class Calculator {

    public int add(int val1, int val2) {
        return val1 + val2;
    }

    public int multiply(int val1, int val2) {
        return val1 * val2;
    }

    public double divide(int val1, int val2) {
        return (double) val1 / val2;
    }
}
